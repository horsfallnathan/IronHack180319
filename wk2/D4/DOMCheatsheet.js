// To select an element in the document

//Get an HTML collection of elements by its tag name
document.getElementsByTagName('name');

//Get an HTML collection of elements matching class
document.getElementsByClassName('.name');

//Get an HTML collection of elements matching name
document.getElementsByName('name');

//Get an HTML collection of elements by its tag name
document.getElementByTagId('#id');

//Get the first element matching selector
document.querySelector('name');

//Get a nodelist of elements matching selector
document.querySelectorAll('name');

